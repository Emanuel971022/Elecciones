package negocio;

/**
 *
 * @author Estudiante
 */
public class Miembro {
    private String cc, nombre, telefono, gustos;

    public Miembro(String cc, String nombre, String telefono, String gustos) {
        this.cc = cc;
        this.nombre = nombre;
        this.telefono = telefono;
        this.gustos = gustos;
    }

    public String getCc() {
        return cc;
    }

    public void setCc(String cc) {
        this.cc = cc;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getGustos() {
        return gustos;
    }

    public void setGustos(String gustos) {
        this.gustos = gustos;
    }
}
